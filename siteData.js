function renderHero(data) {
  const eyebrowEl = document.querySelector('.hero-eyebrow');
  if(eyebrowEl) eyebrowEl.textContent = data.hero.eyebrow;

  const titleEl = document.querySelector('.hero-title');
  if(titleEl) {
    titleEl.innerHTML = `
      <span class="line"><span>${data.hero.titleLine1}</span></span>
      <span class="line"><span>${data.hero.titleLine2}</span></span>
      <span class="line"><span>${data.hero.titleLine3}</span></span>
    `;
  }

  const subtextEl = document.querySelector('.hero-meta-left');
  if(subtextEl) subtextEl.textContent = data.hero.meta;

  const statsEl = document.querySelector('.hero-stats');
  if(statsEl) {
    statsEl.innerHTML = data.hero.stats.map(stat => `
      <div class="hero-stat"><strong>${stat.num}</strong><span>${stat.label}</span></div>
    `).join('');
  }

  const sliderEl = document.getElementById('heroSlider');
  if(sliderEl) {
    sliderEl.innerHTML = data.hero.videos.map((vid, idx) => `
      <div class="hero-slide ${idx === 0 ? 'active' : ''}">
        <video muted ${idx === 0 ? 'autoplay' : ''} playsinline>
          <source src="${vid.webm}" type="video/webm">
          <source src="${vid.mp4}" type="video/mp4">
        </video>
        <div class="hero-slide-overlay"></div>
      </div>
    `).join('');
  }

  const indicatorsEl = document.getElementById('heroIndicators');
  if(indicatorsEl) {
    indicatorsEl.innerHTML = data.hero.videos.map((_, idx) => `
      <button class="hero-indicator ${idx === 0 ? 'active' : ''}" data-slide="${idx}" aria-label="Slayt ${idx + 1}"></button>
    `).join('');
  }
}

